# Built-in functions
print('Hello, World!') # prints the text Hello, World!
print(len('Hello, World!')) # counts the number of characters including spaces
print(type('Hello, World!')) # checks the data type
print(str(10)) # converts number to string
print(int('10')) # converts string to int
print(float(10)) # converts integer to decimal
# input('Enter your name:') # takes user input

print(min(20, 30, 40, 50)) # gives the minimum value
print(max(20, 30, 40, 50)) # gives the maximum value
print(min([20, 30, 40, 50])) # it takes the list as an argument and returns the min value
print(max([20, 30, 40, 50])) # taks the list as an argument and returns the max value
print(sum([20, 30, 40, 50])) #it takes only the list as an argument and returns the sum

